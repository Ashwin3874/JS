// Arithmetic Operators:

// (+) Addition
// (-) Subtraction
// (*) Multiplication
// (/) Division
// (%) Modulus,returns the remainder
// (**) Exponentiation, performs exponential calculations
// (++) Increment, increases a value by one
// (--) Decrement, decreases a value by one

x = 345;
y = 67;

console.log("x + y = " + (x + y));
console.log("x - y = " + (x - y));
console.log("x * y = " + (x * y));
console.log("x / y = " + (x / y));
console.log("x % y = " + (x % y));
console.log("x ** y = " + (x ** 2));
console.log("++x = " + (++x));
console.log("--y = " + (--y));

// Comparison Operators:

// (==) Equal to
// (===) Strict equal to
// (!=) Not equal to
// (!==) Strict not equal to
// (>) Greater than
// (<) Less than
// (>=) Greater than or equal to
// (<=) Less than or equal to   

a = 10;
b = 10;

console.log("a == b: " + (a == b));
console.log("a === b: " + (a === b));
console.log("a != b: " + (a != b));
console.log("a !== b: " + (a !== b));
console.log("a > b: " + (a > b));
console.log("a < b: " + (a < b));
console.log("a >= b: " + (a >= b));
console.log("a <= b: " + (a <= b));

// Logical Operators:

// (&&) Logical AND
// (||) Logical OR
// (!) Logical NOT
// Logical XOR (exclusive OR)

console.log("(a > 5 && b < 15): " + (a > 5 && b < 15));
console.log("(a < 5 || b < 15): " + (a < 5 || b < 15));
console.log("!(a == b): " + (!(a == b)));
console.log("Logical XOR (a > 5 ^ b < 15): " + ((a > 5) ^ (b < 15)));
